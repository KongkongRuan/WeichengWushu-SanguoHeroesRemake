// ============================================================
// HP 计算 (还原 a.java 中的公式)
// ============================================================

/**
 * 计算敌人HP
 * @param level 关卡索引 (0-10)
 * @param variant 敌人变体 (来自 m1129 或 -1)
 * @param enemyType 敌人类型 (16或18时除以2)
 * @returns HP值
 */
export function calculateEnemyHP(level: number, variant: number, enemyType: number = 0): number {
  const [base, slope] = ENEMY_HP_DATA[level] || ENEMY_HP_DATA[0];
  let hp = slope * variant + base;
  // 敌人类型16和18: HP减半
  if (enemyType === 16 || enemyType === 18) {
    hp = hp >> 1;
  }
  return hp;
}

/**
 * 计算备用HP (用于s1102数组)
 */
export function calculateAltHP(level: number, variant: number): number {
  const [base, slope] = ALT_HP_DATA[level] || ALT_HP_DATA[0];
  return slope * variant + base;
}

